#Faça um Programa que pergunte quanto você ganha por hora e o número de horas trabalhadas no mês. Calcule e mostre o total do seu salário no referido mês.
sal = float(input('Informe quanto você recebe por hora trabalhada: '))
hr = float(input('Informe quantas horas você trabalha por mês: '))
print(f'O seu salario com as informações repassadas será de R${sal*hr}.')